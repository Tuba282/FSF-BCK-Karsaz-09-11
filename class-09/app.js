// marks > 90 == A1 grade
// marks > 80 == A grade
// marks > 70 == B grade
// marks > 60 == C grade
// marks > 50 == D grade
// otherwise == Fail


let marks = 78

if (marks > 90) {
    console.log(`Grade = A1`);
} else if (marks > 80) {
    console.log(`Grade = A`);
} else if (marks > 70) {
    console.log(`Grade = B`);
} else if (marks > 60) {
    console.log(`Grade = C`);
} else if (marks > 50) {
    console.log(`Grade = D`);
} else {
    console.log(`Fail`);
}

